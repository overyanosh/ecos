#!/bin/bash
set -euo pipefail

# === ecOS GRUB Splash Setup ===
echo "[ecOS] Configuring GRUB splash screen..."

GRUB_DIR="/boot/grub2"
LOGO_DIR="/usr/share/ecos"
SPLASH_IMAGE="$LOGO_DIR/splash.png"

# Créer le dossier avec le logo
mkdir -p "$LOGO_DIR"
# Le logo sera copié depuis /usr/etc/assets pendant le Containerfile build

# Vérifier que l'image existe
if [[ ! -f "$SPLASH_IMAGE" ]]; then
    echo "[ecOS] WARNING: splash image not found at $SPLASH_IMAGE"
    echo "[ecOS] Skipping GRUB splash configuration"
    exit 0
fi

# Redimensionner l'image pour GRUB (640x480 recommandé)
# On utilise convert si ImageMagick est dispo, sinon on garde l'image originale
if command -v convert &>/dev/null; then
    echo "[ecOS] Resizing splash image for GRUB compatibility..."
    mkdir -p "$LOGO_DIR/resized"
    convert "$SPLASH_IMAGE" -resize 640x480! "$LOGO_DIR/resized/splash-640x480.png"
    SPLASH_FINAL="$LOGO_DIR/resized/splash-640x480.png"
else
    echo "[ecOS] ImageMagick not available — using original splash image"
    SPLASH_FINAL="$SPLASH_IMAGE"
fi

# Copier l'image vers /boot/grub2 (pour que GRUB la trouve facilement)
cp "$SPLASH_FINAL" "$GRUB_DIR/splash.png"

# Générer le hash de l'image (pour GRUB)
SPLASH_HASH=$(sha256sum "$GRUB_DIR/splash.png" | awk '{print $1}')

# Créer ou modifier /etc/default/grub_custom pour override
GRUB_CUSTOM="/etc/default/grub_custom"
cat > "$GRUB_CUSTOM" << EOF
# === ecOS GRUB Custom Configuration ===
# Generated by ecos-grub-setup.sh on $(date)

# Splash image
GRUB_SPLASH="$GRUB_DIR/splash.png"
GRUB_BACKGROUND="$GRUB_DIR/splash.png"

# Couleur du texte (blanc pour contraste sur logo foncé)
GRUB_COLOR_NORMAL="white/black"
GRUB_COLOR_HIGHLIGHT="yellow/blue"

# Timeout réduit pour boot rapide
GRUB_TIMEOUT=5
GRUB_TIMEOUT_STYLE=menu

# Mode graphique
GRUB_GFXMODE=auto
EOF

# Recompiler GRUB
echo "[ecOS] Regenerating GRUB configuration..."
grub2-mkconfig -o /boot/grub2/grub.cfg 2>/dev/null || \
    grub2-mkconfig -o /boot/efi/EFI/almalinux/grub.cfg 2>/dev/null || \
    echo "[ecOS] WARNING: Could not regenerate GRUB config — manual reboot required"

echo "[ecOS] GRUB splash configured successfully!"
echo "[ecOS] The ecOS logo will appear at boot."